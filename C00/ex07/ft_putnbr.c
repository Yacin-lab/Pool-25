#include <unistd.h>

void put_char(int nm)
{
	char c;
	if (nm >= 10)
		put_char(nm / 10);
	c = (nm % 10) + '0';
	write(1, &c, 1);
}

void ft_putnbr(int nb)
{
	if(nb == -2147483648)
	{
		write(1, "-2147483648", 11);
		return;
	}
	if(nb < 0)
	{
		write(1, "-", 1);
		nb = -nb;
	}

	put_char(nb);
}
