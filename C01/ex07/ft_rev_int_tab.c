void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	j;
	int	rev;

	j = size - 1;
	i = 0;
	while (i < j)
	{
		rev = tab[i];
		tab[i] = tab[j];
		tab[j] = rev;
		i++;
		j--;
	}
}
